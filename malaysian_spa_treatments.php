            <h2 class="h2">Tangas</h2>

            <p>
              Tangas with astringent herbs (requires a special single bench and earthen pot to boil the herbs) are traditionally used by young women near the end of their menstrual cycle to help reduce excessive white discharge, fungal infections, and unpleasant odour. Tangas is also used in postnatal treatments and is thought to firm up vaginal muscles and reduce inflammation.
