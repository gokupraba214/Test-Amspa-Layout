            <h2 class="h2">THAI MASSAGE</h2>

            <p>
             In Thailand, this is known as nuat phaen boran, which translates to "ancient or traditional massage." Thai massage is a type of massage that originated in India and is based on ayurveda and yoga. During the massage, the technique combines massage with yoga-like positions. Stretching is emphasised in the northern style, while acupressure is emphasised in the southern style.
            </p>

