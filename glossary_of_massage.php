  <img class="entryimage" src="images/Glossary of Massages/DEEP TISSUE MASSAGE.jpg" alt="DEEP TISSUE MASSAGE">
        
        <div class="entrycontent">

            <h2 class="h2">DEEP TISSUE MASSAGE</h2>

            <p>   
         Deep tissue massage combines Swedish massage movements with specialised finger and thumb techniques aimed at releasing tension held in the body's connective tissues. These precise penetrating movements effectively loosen contracted and stressed muscles, aid in the release of adhesions, and provide a general sense of deep relaxation. Deep tissue massage is frequently recommended for people who have chronic aches and pains, as well as those who engage in strenuous physical activity and sustain physical injuries, such as athletes.
		 </p>
        </div>

    </div>

    <hr>

     <div class="entry reverse">
        
        <div class="entrycontent">

            <h2 class="h2">MEDICAL MASSAGE</h2>

            <p>
             Massage in the medical field includes decongestive therapy for lymph oedema, which can be used in conjunction with breast cancer treatment. Carotid sinus massage is used to diagnose carotid sinus syncope and can help distinguish supraventicular tachycardia (SVT) from ventricular tachycardia (VT). It, like the valsalva manoeuvre, is used to treat SVT. However, it is less effective than medication-based SVT management.
            </p>

        </div>

  <img src="images/Glossary of Massages/MEDICAL MASSAGE.jpg" class="entryimage" alt="MEDICAL MASSAGE">

