            <h2 class="h2">REFLEXOLOGY</h2>

            <p>
             Reflexology is a Chinese traditional massage therapy that is based on the idea that there are reflexes in the hands and feet that correspond to every organ, gland, and system in the body. The most common treatment focuses on applying pressure to the various reflexology points on the feet to induce soothing relaxation.
            </p>

        </div>

    </div>

    <hr>

      <div class="entry reverse">
        
        <div class="entrycontent">

            <h2 class="h2">SHIATSU</h2>

            <p>
             Shiatsu (shi means 'finger' and atsu means 'pressure') is an eastern therapy that incorporates stretching as well as pressure applied with thumbs, fingers, and palms to the same energy meridians as acupressure. It also employs rolling, brushing, vibrating, and grasping techniques. The client's back, legs, and feet are pressed.
            </p>

        </div>
    
        <img src="images/Glossary of Massages/SHIATSU.jpg" class="entryimage" alt="SHIATSU">

    </div>

    <hr>


    <div class="entry">

        <img class="entryimage" src="images/Glossary of Massages/SWEDISH MASSAGE.jpg" alt="SWEDISH MASSAGE">
        
        <div class="entrycontent">

            <h2 class="h2">SWEDISH MASSAGE</h2>

            <p>
             Swedish massage is a technique that is used in most spas around the world to perform classic western massage. Swedish massage, developed in the early 1800s by Per Henrik Ling and refined by Johan Georg Mezger, is based on a series of comfortable flowing movements such as effleurage, petrissage, tapotement, and friction. These relaxing massage techniques have been shown to relieve general muscle aches and pains, joint stiffness, poor circulation, and stress.
            </p>

        </div>

    </div>

    <hr>

    <div class="entry reverse">
        
        <div class="entrycontent">

            <h2 class="h2">STONE MASSAGE</h2>

            <p>
               Stone massage employs the use of cold or heated stones to apply pressure and heat to the body. The therapist can also use oil-coated stones to deliver various massage strokes. The stones are typically river stones that have become extremely polished and smooth over time. The heat from the stones penetrates the muscles, releasing tension, as they are placed along the recipient's back.
            </p>

        </div>
    
        <img src="images/Glossary of Massages/STONE MASSAGE.jpg" class="entryimage" alt="STONE MASSAGE">

    </div>

