        <div class="entrycontent">

            <h2 class="h2">CHINESE TRADITIONAL MASSAGE</h2>

            <p>
              Traditional Chinese massages are classified into two types: Tui na, which focuses on pushing, stretching, and kneading the muscles, and Zhi Ya, which focuses on pinching and pressing at acupressure points. Both are based on Traditional Chinese Medicine principles (TCM).
            </p>

        </div>

        <img src="images/Glossary of Massages/CHINESE TRADITIONAL MASSAGE.jpg" class="entryimage" alt="CHINESE TRADITIONAL MASSAGE">

