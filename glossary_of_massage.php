            <h2 class="h2">AYURVEDIC MASSAGE</h2>

            <p>
              Ayurveda is a natural health care system that originated in India and has been practised for thousands of years. Massage, yoga, meditation, and herbal remedies are all part of it. Abhyanga is a type of Ayurvedic massage that is typically performed by one or two therapists using a heated blend of herbal oils based on the ayurvedic dosha system.
            </p>
        </div>

